#pide al usuario tres notas , calcula el promedio de las tres notas e imprime el resultado

not1 = int(input("Ingrese su primera nota: "))
not2 = int(input("Ingrese su segunda nota: "))
not3 = int(input("Ingrese su tercera nota: "))

promedio = (not1 + not2 + not3) // 3
print(f"tu promedio es {promedio}")

